// Language names for display
const languageNames = {
    en: "English",
    zh: "中文 (Chinese)",
    ja: "日本語 (Japanese)", 
    ko: "한국어 (Korean)",
    th: "ไทย (Thai)",
    my: "မြန်မာ (Myanmar)"
};

// Default language
let currentLanguage = 'en';

// Function to get translation
function t(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}

// Function to change language
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('airportAppLanguage', lang);
    updateUI();
}

// Function to load saved language
function loadLanguage() {
    const savedLang = localStorage.getItem('airportAppLanguage');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    }
}

// Function to update UI with new language
function updateUI() {
    // Update navigation
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = t(key);
    });
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = t(key);
    });
    
    // Update current language display
    const currentLangElement = document.getElementById('current-language');
    if (currentLangElement) {
        currentLangElement.textContent = languageNames[currentLanguage];
    }
}